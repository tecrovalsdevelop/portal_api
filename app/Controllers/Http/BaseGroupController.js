'use strict'
const ResponseHelper = use('App/Helpers/ResponseHelper')
const baseGroup = use('App/Models/BaseGroup')

/** @typedef {import('@adonisjs/framework/src/Request')} Request */
/** @typedef {import('@adonisjs/framework/src/Response')} Response */
/** @typedef {import('@adonisjs/framework/src/View')} View */

/**
 * Resourceful controller for interacting with basegroups
 */
class BaseGroupController {
    /**
     * Show a list of all basegroups.
     * GET basegroups
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     * @param {View} ctx.view
     */
    async index({ request, response, view }) {

        try {
            return ResponseHelper.getOnlyDataResponse(await baseGroup.all())
        } catch (err) {
            //console.log(err.message)
            return ResponseHelper.getErrorResponse("Dados n√£o encontrados")
        }
    }

    /**
     * Render a form to be used for creating a new basegroup.
     * GET basegroups/create
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     * @param {View} ctx.view
     */
    async create({ request, response, view }) {}

    /**
     * Create/save a new basegroup.
     * POST basegroups
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     */
    async store({ request, response }) {}

    /**
     * Display a single basegroup.
     * GET basegroups/:id
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     * @param {View} ctx.view
     */
    async show({ params, request, response, view }) {}

    /**
     * Render a form to update an existing basegroup.
     * GET basegroups/:id/edit
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     * @param {View} ctx.view
     */
    async edit({ params, request, response, view }) {}

    /**
     * Update basegroup details.
     * PUT or PATCH basegroups/:id
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     */
    async update({ params, request, response }) {}

    /**
     * Delete a basegroup with id.
     * DELETE basegroups/:id
     *
     * @param {object} ctx
     * @param {Request} ctx.request
     * @param {Response} ctx.response
     */
    async destroy({ params, request, response }) {}
}

module.exports = BaseGroupController